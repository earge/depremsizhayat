        public override string[] GetRolesForUser(string mail)
        {
            var userService = DependencyResolver.Current.GetService<IUserService>();
            var roleService = DependencyResolver.Current.GetService<IRoleService>();
            var userRoles = (from user in userService.GetAll()
                             join role in roleService.GetAll()
                             on user.ROLE_ID equals role.ROLE_ID
                             where user.E_MAIL == mail
                             select role.NAME).ToArray();
            return userRoles;
        }

